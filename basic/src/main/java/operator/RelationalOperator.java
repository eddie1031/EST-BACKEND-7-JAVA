package operator;

public class RelationalOperator {

    public static void main(String[] args) {

        // # 관계 연산자
        int big = 10;
        int small = 5;

        System.out.println(big > small); // true
        System.out.println(big < small); // false
        System.out.println(big <= small); // false
        System.out.println(big >= small); // true
        System.out.println(big == small); // false
        System.out.println(big != small); // true

    }

}
