package variable;

public class Variable1 {

    public static void main(String[] args) {

        // 선언(Declare)
        int integerVariable;
        // 할당(Allocate)
        integerVariable = 10;

        // 초기화(Initialize)
        char characterVariable = 'A';

        // 같은 타입의 여러 변수 선언
        int age, height, weight;
        age = 10;
        height = 20;
        weight = 30;

        // 같은 타입의 여러 변수 초기화
        int w = 1980, h = 1080;

    }

}
